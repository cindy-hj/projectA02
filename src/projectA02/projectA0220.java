package projectA02;

public class projectA0220 {

	public static void main(String[] args) {
		for(int i=0; i<10; i++) {
			System.out.print(i);
		}
		System.out.println(); // 줄바꾸기
		int j;
		for(j=0; j<10; j++) {
			System.out.print(j);
		}
		System.out.println();
		// 0~100까지 출력
		for (int k=0; k<=100; k++) {
			System.out.print(k);
		}
		System.out.println();
		int l=0; // 선언하면서 초기화 시키는것이 기본
		for(l=0; l<=100; l++) {
			System.out.print(l);
		}
		System.out.println();
	}

}
